pub const Opcodes = enum(u8) {
    // System / Traps
    invalid = 0x00,
    nop = 0x40,
    halt = 0x01,
    trap = 0x41,

    // Control flow
    jmps = 0x02,
    jmpl = 0x42,
    hops = 0x03,
    hopl = 0x43,
    calls = 0x04,
    calll = 0x44,
    rets = 0x05,
    retl = 0x45,
    jnzs = 0x06,
    jnzl = 0x46,

    // Comparison
    cmpr = 0x07, // Note: takes immediate comparison type byte

    // Stack manipulation
    push1 = 0x08,
    push2 = 0x48,
    drop1 = 0x09,
    drop2 = 0x49,
    dup1 = 0x0A,
    dup2 = 0x4A,
    swap1 = 0x0B,
    swap2 = 0x4B,
    nip1 = 0x0C,
    nip2 = 0x4C,
    over1 = 0x0D,
    over2 = 0x4D,
    rot1 = 0x0E,
    rot2 = 0x4E,
    pick1 = 0x0F,
    pick2 = 0x4F,
    poke1 = 0x10,
    poke2 = 0x50,

    // Arithmetic
    add1 = 0x11,
    add2 = 0x51,
    sub1 = 0x12,
    sub2 = 0x52,
    mul1 = 0x13,
    mul2 = 0x53,
    div1 = 0x14,
    div2 = 0x54,
    mod1 = 0x15,
    mod2 = 0x55,
    neg1 = 0x16,
    neg2 = 0x56,
    abs1 = 0x17,
    abs2 = 0x57,
    inc1 = 0x18,
    inc2 = 0x58,
    dec1 = 0x19,
    dec2 = 0x59,

    // Bitwise
    and1 = 0x1A,
    and2 = 0x5A,
    or1 = 0x1B,
    or2 = 0x5B,
    not1 = 0x1C,
    not2 = 0x5C,
    xor1 = 0x1D,
    xor2 = 0x5D,
    shl1 = 0x1E,
    shl2 = 0x5E,
    shr1 = 0x1F,
    shr2 = 0x5F,
    rol1 = 0x20,
    rol2 = 0x60,
    ror1 = 0x21,
    ror2 = 0x61,

    // Memory access
    load1 = 0x22,
    load2 = 0x62,
    store1 = 0x23,
    store2 = 0x63,
    iload1 = 0x24,
    iload2 = 0x64,
    istore1 = 0x25,
    istore2 = 0x65,
    memcpy1 = 0x26,
    memcpy2 = 0x66,
    memset1 = 0x27,
    memset2 = 0x67,
};
